package org.jata.write.sample;

public class Button {

	Color color = Color.BLUE;

	String name = "submit";

	public Button() {
	}

	public Color getColor() {
		return color;
	}

	public void setColor(Color color) {
		this.color = color;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}

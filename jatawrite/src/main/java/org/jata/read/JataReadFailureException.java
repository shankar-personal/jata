package org.jata.read;

/**
 *
 *
 */
public class JataReadFailureException extends RuntimeException {

	/**
	 *
	 */
	private static final long serialVersionUID = 1L;

	public JataReadFailureException() {
		super();

	}

	public JataReadFailureException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public JataReadFailureException(String arg0) {
		super(arg0);

	}

	public JataReadFailureException(Throwable arg0) {
		super(arg0);

	}

}

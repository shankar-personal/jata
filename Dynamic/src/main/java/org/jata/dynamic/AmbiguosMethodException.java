package org.jata.dynamic;

/**
 * @author Shankar Narayan
 *
 */
public class AmbiguosMethodException extends Exception {

	public AmbiguosMethodException() {
		super();
	}

	public AmbiguosMethodException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AmbiguosMethodException(String arg0) {
		super(arg0);
	}

	public AmbiguosMethodException(Throwable arg0) {
		super(arg0);
	}

}
